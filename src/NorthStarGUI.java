    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        if (txtOldPassword.getText().isEmpty() || txtNewPassword.getText().isEmpty() || txtConfirmPassword.getText().isEmpty()){
            lblChangePasswordWarning.setText("Please enter all fields.");
        }
        else if (!txtOldPassword.getText().contentEquals(this.pw)){
            lblChangePasswordWarning.setText("Old password is incorrect.");
            txtOldPassword.setText("");
            txtNewPassword.setText("");
            txtConfirmPassword.setText("");
        }
        else if (!txtNewPassword.getText().contentEquals(txtConfirmPassword.getText())){
            lblChangePasswordWarning.setText("New passwords do not match.");
            txtNewPassword.setText("");
            txtConfirmPassword.setText("");
        }
        else if(!(txtNewPassword.getText().contains("1")||txtNewPassword.getText().contains("2")||txtNewPassword.getText().contains("3")||txtNewPassword.getText().contains("4")||txtNewPassword.getText().contains("5")||txtNewPassword.getText().contains("6")||txtNewPassword.getText().contains("7")||txtNewPassword.getText().contains("8")||txtNewPassword.getText().contains("9")||txtNewPassword.getText().contains("0"))){
            lblChangePasswordWarning.setText("Password must contain a number.");
            txtNewPassword.setText("");
            txtConfirmPassword.setText("");
        }
        else{
            int n;
            n = JOptionPane.showConfirmDialog(jOptionPane1, "Are you sure you want to change your password?", "Confirm Passsword Change", JOptionPane.YES_NO_OPTION);
            if(n == 0) {
                this.pw = txtNewPassword.getText();
                JOptionPane.showMessageDialog(jOptionPane1, "Your password has been successfully changed.");
                txtOldPassword.setText("");
                txtNewPassword.setText("");
                txtConfirmPassword.setText("");
                lblChangePasswordWarning.setText("");
            }
        }
    }//GEN-LAST:event_jButton13ActionPerformed

