    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        if (txtCardHolder.getText().isEmpty() || txtCardNumber.getText().isEmpty() || txtPayment.getText().isEmpty()){
            lblPayWarning.setText("Please fill out all payment information.");
        }
        else if (txtCardNumber.getText().length() != 16){
            lblPayWarning.setText("Card number is invalid.");
        }
        else if (Float.parseFloat(txtPayment.getText()) < 200){
            lblPayWarning.setText("Must pay at least the minimum due.");
        }
        else{
            int n;
            n = JOptionPane.showConfirmDialog(jOptionPane1, "Make a payment of $"+txtPayment.getText()+"?", "Confirm Payment Amount", JOptionPane.YES_NO_OPTION);
            if(n == 0) {
                jLabel44.setText(Float.toString(Float.parseFloat(jLabel44.getText())-Float.parseFloat(txtPayment.getText())));
                jLabel45.setText("0.00");
                lblPayWarning.setText("");
            }
        }
    }//GEN-LAST:event_jButton11ActionPerformed

